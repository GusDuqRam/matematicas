def perRectangulo ():
    base= float(input("Ingrese el valor de la base: "))   
    altura= float(input("Ingrese el valor de la altura: "))          
    return print("El perimetro del restanfulo: ", 2*base + 2*altura) # 16 de Julio de 2023
perRectangulo() # 18 de Julio de 2023

